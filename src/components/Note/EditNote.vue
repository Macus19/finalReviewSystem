<template>
  <div class="edit-note-container">
      <el-row class="title-bar" flex>
          <el-col :span="4">笔记标题：</el-col>
          <el-col :span="10">
              <el-input v-model="title" placeholder="标题"></el-input>
          </el-col>
          <el-col :span="8">
            <div class="btn-list">
                <el-button @click="editNote">发布笔记</el-button>
            </div>
          </el-col>
      </el-row>
      <quill-editor class="editor"
                    ref="myTextEditor"
                    v-model="content"
                    :options="editorOption"
                    @blur="onEditorBlur($event)"
                    @focus="onEditorFocus($event)"
                    @ready="onEditorReady($event)"
                    @change="onEditorChange($event)">
      </quill-editor>
  </div>
</template>

<script>
export default {
  data() {
    return {
      title: '',
      content: '',
    };
  },

  components: {},

  methods: {
    editNote() {
      this.axios({
        url: `${process.env.API_HOST}/note/addNote`,
        method: 'POST',
        data: {
          token: sessionStorage.getItem('token'),
          data: {
            note: {
              title: this.title,
              content: this.content,
            },
          },
        },
      }).then((res) => {
        if (res.data.code === 0) {
          this.$message({
            message: '发布成功！',
            type: 'success',
          });
          this.$router.push({
            path: '/PersonalZone',
          });
        }
      });
    },
  },
};

</script>
